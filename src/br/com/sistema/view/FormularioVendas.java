    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
String nome = txtProduto.getText();
Produtos obj = new Produtos();
ProdutosDAO daop = new ProdutosDAO();

obj = daop.BuscarProdutos(nome);
if (obj.getDescricao() != null) {
    int estoque = Integer.valueOf(txtEstoque.getText());
    int quantidade = Integer.valueOf(txtQuantidade.getText());
    preco = Double.valueOf(txtPreco.getText());
    qtd = Integer.valueOf(txtQuantidade.getText());
    subtotal = preco * qtd;
    total += subtotal;
    if (estoque >= quantidade) {
        txtTotal.setText(String.valueOf(total));
        meus_produtos = (DefaultTableModel) tabelaCarrinho.getModel();
        meus_produtos.addRow(new Object[]{
            txtCodigo.getText(),
            txtProduto.getText(),
            txtQuantidade.getText(),
            txtPreco.getText(),
            subtotal
        });
    } else {
        JOptionPane.showMessageDialog(null, "A quantidade desejada é maior do que a tem no estoque!");
    }
} else { 
    JOptionPane.showMessageDialog(null, "Falta de Informações, não foi possivel adicionar ao carrinho");
}
    }//GEN-LAST:event_btnAdicionarActionPerformed

    private void btnPagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPagarActionPerformed
        String nome = txtNome.getText();
        String cpf = txtCpf.getText();
        obj = new Clientes();
        ClientesDAO daoc = new ClientesDAO();
        obj = daoc.BuscarCliente(nome);
        obj = daoc.BuscarClienteCPF(cpf);
        if(obj.getNome () != null && obj.getCpf() != null){
            FormularioPagamentos telaPag = new FormularioPagamentos();
            telaPag.clientes = obj;
            telaPag.meus_produtos = meus_produtos;
            telaPag.txtTotalVenda.setText(String.valueOf(total));
            telaPag.setVisible(true);
            this.dispose();
            
        }else{
            JOptionPane.showMessageDialog(null,"Verifique se os campos de Nome e CPF do cliente estão preenchidos.");
        }
    }//GEN-LAST:event_btnPagarActionPerformed
    
 
