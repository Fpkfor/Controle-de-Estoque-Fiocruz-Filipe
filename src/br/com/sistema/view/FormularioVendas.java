    private void txtProcurarProdutoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtProcurarProdutoKeyReleased
        String nome = "%" + txtProcurarProduto.getText()+"%";     
        
        ProdutosDAO dao = new ProdutosDAO();
        List <Produtos> lista = dao.Filtrar(nome);
            DefaultTableModel dados = (DefaultTableModel)tabelaProduto.getModel();
            dados.setNumRows(0);
            for(Produtos p : lista) { 
                dados.addRow(new Object[]{
                p.getId(),
                p.getDescricao(),
                p.getPreco(),
                p.getQtd_estoque(),
                p.getFornecedores().getNome()
        });
        txtCodigo.setEnabled(false);
    }
    }//GEN-LAST:event_txtProcurarProdutoKeyReleased

